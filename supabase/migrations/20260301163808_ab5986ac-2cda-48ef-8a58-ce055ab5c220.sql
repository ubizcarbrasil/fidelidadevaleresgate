  WHEN name ILIKE '%pizza%' THEN 'pizza'
  WHEN name ILIKE '%hambur%' THEN 'beef'
  WHEN name ILIKE '%café%' OR name ILIKE '%cafeteria%' THEN 'coffee'
  WHEN name ILIKE '%padaria%' THEN 'croissant'
