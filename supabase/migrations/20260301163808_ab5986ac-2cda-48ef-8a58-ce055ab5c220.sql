  WHEN name ILIKE '%academia%' OR name ILIKE '%fitness%' THEN 'dumbbell'
  WHEN name ILIKE '%salão%' OR name ILIKE '%barbearia%' OR name ILIKE '%cabeleir%' THEN 'scissors'
